// "type-conversion". 

// In many programming language inclusive of JavaScript, whenever we try to 
// do an operation, based on the input we can actually convert the input for 
// operation. (Convert means we can convert type of input). This conversion
// can be manually done by us or the language based on some certain rules
// automatically converts the type.
// (a) Explicit Typecasting : The process of converting the types manually 
//     ourselves is called typecasting. 
// (b) Implicit Typecasting : The process of converting the types based on
//     certain set of rules automatically converts the types is called as
//     implicit typecasting or Coercion.
// Note : Typecasting is also referred as typeconversion.