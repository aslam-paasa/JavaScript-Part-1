// What are abstract operations?
// 1. These are some set of algorithms, that is present in the ECMAScript
//    docs, but they are not available for usage in ECMAScript i.e. we as
//    developers cannot use these operations directly.
// 2. They are mentioned in the docs to aid/help the documentation only.
//    In the ECMA docs there are a lot things that are done by the language
//    internally. To explain these internal details of how & what language
//    is doing, we have abstract operations mentioned in the docs (to help
//    or make the explanation easy, we have asbtract operations).