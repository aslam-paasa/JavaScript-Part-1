/* Basics of JavaScript :
 - JavaScript is a scripting language that enables you to create dyanmically
   updating content, control multimedia, animate images, and much more.

Q) Where to put?
 - In HTML, JavaScript code is inserted between <script> and </script>

    <script>
            document.getElementById("demo").innerHTML = "My First JavaScript";
    </script>

 - You can place any number of scripts in an HTML document.
 - Scripts can be placed in the <body>, or in the <head> section of an HTML page,
   or in both.
 - We can also create separate External JavaScript :

    <script src="myScript.js"></script>
+-------------------------------------------------------------------------------+*/ 