/**
 * Special Characters :
 *  1. \\ - Backslash
 *  2. \b - Backspace
 *  3. \r - Carriage return
 *  4. \" - Double quote
 *  5. \f - Form feed
 *  6. \n - New Line
 *  7. \' - Single quote
 *  8. \t - Tab
*/ 


/**
 * 1. New Line:
*/
let text = "This new apple iphone \n has been launched";
console.log(text);

/**
 * 2. Gap of a tab:
*/
let text2 = "This new apple iphone 15 \t has been launched";
console.log(text2);
